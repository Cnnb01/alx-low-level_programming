/**
 * add - Adds two integers and returns the result.
 * @m: The first integer.
 * @n: The second integer.
 *
 * Description: function takes two integers as input and returns the sum.
 * Return: The sum of the two integers.
*/
int add(int m, int n)
{
int sum;
sum = m + n;
return (sum);
}
